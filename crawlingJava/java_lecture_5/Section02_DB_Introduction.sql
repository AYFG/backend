-- [강의: 데이터와 정보]
-- 데이터(Data): 가공되지 않은 원시 사실 (예: "홍길동", 25, "서울")
-- 정보(Information): 데이터를 가공하여 의미를 부여한 것 (예: "25세 홍길동은 서울에 거주")

-- [강의: 데이터베이스 관리 시스템이 필요한 이유]
-- 파일 시스템의 한계:
--   1. 데이터 중복: 같은 데이터가 여러 파일에 흩어져 있음
--   2. 데이터 불일치: 한 곳을 수정해도 다른 곳은 그대로 → 정합성 문제
--   3. 동시 접근 어려움: 여러 사용자가 같은 파일에 접근하면 충돌
-- → 이러한 문제를 해결하기 위해 DBMS(Database Management System)가 탄생

-- [강의: DBMS 소개]
-- DBMS: 데이터를 체계적으로 저장, 관리, 검색할 수 있는 소프트웨어
-- 대표적인 DBMS: MySQL, PostgreSQL, Oracle, SQL Server, SQLite

-- [강의: 관계형 데이터베이스 vs NoSQL]
-- 관계형 DB (RDBMS):
--   - 데이터를 테이블(표) 형태로 저장
--   - SQL 언어를 사용
--   - 데이터 간의 관계를 정의할 수 있음
--   - 예: MySQL, PostgreSQL, Oracle
--
-- NoSQL:
--   - 테이블 형태가 아닌 다양한 형태로 저장 (문서, 키-값, 그래프 등)
--   - 대규모 분산 환경에 유리
--   - 예: MongoDB(문서형), Redis(키-값형), Neo4j(그래프형)

-- [강의: 관계형 데이터베이스 핵심 개념]
-- 테이블(Table): 데이터를 저장하는 기본 단위 (행과 열로 구성)
-- 행(Row/Record): 하나의 데이터 (예: 한 명의 회원 정보)
-- 열(Column/Field): 데이터의 속성 (예: 이름, 나이, 이메일)
-- 기본키(Primary Key, PK): 각 행을 고유하게 식별하는 값 (중복 불가, NULL 불가)
-- 외래키(Foreign Key, FK): 다른 테이블의 기본키를 참조하는 값 (테이블 간 관계 연결)
